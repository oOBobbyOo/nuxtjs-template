<script setup lang="ts">
import '@simonwep/pickr/dist/themes/classic.min.css'
import '@simonwep/pickr/dist/themes/monolith.min.css'
import '@simonwep/pickr/dist/themes/nano.min.css'

import Pickr from '@simonwep/pickr'

defineOptions({ name: 'ColorPickr' })

const props = defineProps<{
  modelValue?: string
  theme: Pickr.Theme
}>()

const emit = defineEmits<{
  (e: 'update:modelValue', value: string): void
}>()

const localColor = ref(props.modelValue)

// 实例
const pickr = ref<Pickr | null>()

// 颜色选择器
const colorPickrRef = ref<HTMLInputElement>()

// Pickr
function createPickr() {
  // if (pickr.value) {
  //   return
  // }

  if (colorPickrRef.value) {
    pickr.value = Pickr.create({
      el: colorPickrRef.value,
      theme: props.theme, // 'classic', or 'monolith', or 'nano'
      swatches: [
        'rgba(244, 67, 54, 1)',
        'rgba(233, 30, 99, 0.95)',
        'rgba(156, 39, 176, 0.9)',
        'rgba(103, 58, 183, 0.85)',
        'rgba(63, 81, 181, 0.8)',
        'rgba(33, 150, 243, 0.75)',
        'rgba(3, 169, 244, 0.7)',
        'rgba(0, 188, 212, 0.7)',
        'rgba(0, 150, 136, 0.75)',
        'rgba(76, 175, 80, 0.8)',
        'rgba(139, 195, 74, 0.85)',
        'rgba(205, 220, 57, 0.9)',
        'rgba(255, 235, 59, 0.95)',
        'rgba(255, 193, 7, 1)',
      ],
      default: localColor.value, // The default color
      components: {
        preview: true,
        opacity: true,
        hue: true,

        interaction: {
          hex: true,
          rgba: true,
          hsla: true,
          hsva: true,
          cmyk: false,
          input: true,
          clear: true,
          save: true,
        },
      },
      i18n: {
        'btn:save': '保存',
        'btn:clear': '清空',
      },
    })
  }
}

onMounted(() => {
  createPickr()

  if (pickr.value) {
    pickr.value.on('save', (color: any) => {
      let hexa = '#000'
      if (color)
        hexa = color.toHEXA().toString()

      localColor.value = hexa
      emit('update:modelValue', hexa)
    })
  }
})

onUnmounted(() => {
  // 销毁实例
  if (pickr.value)
    pickr.value.destroy()
})
</script>

<template>
  <input ref="colorPickrRef" type="text">
</template>

<style scoped lang="less"></style>
